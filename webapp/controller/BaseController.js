sap.ui.define([
	"sap/ui/core/mvc/Controller",
	"com/sodogan/Products/model/formatter/formatter",
	"com/sodogan/Products/model/formatter/obj"
], function (Controller, formatter,Obj) {
	"use strict";

	return Controller.extend("com.sodogan.Products.controller.BaseController", {
		formatter:formatter,
		Obj:Obj,
		/**
		 * Convenience method for accessing the router.
		 * @public
		 * @returns {sap.ui.core.routing.Router} the router for this component
		 */
		getRouter: function () {
			return sap.ui.core.UIComponent.getRouterFor(this);
		},

		/**
		 * Convenience method for getting the view model by name.
		 * @public
		 * @param {string} [sName] the model name
		 * @returns {sap.ui.model.Model} the model instance
		 */
		getModel: function (sName) {
			// pass your component.js to the constructor
            var oComponent = this.getOwnerComponent();
            this._helper = new this.Obj(oComponent);
            
            debugger;
            
			return this.getView().getModel(sName);
		},

		/**
		 * Convenience method for setting the view model.
		 * @public
		 * @param {sap.ui.model.Model} oModel the model instance
		 * @param {string} sName the model name
		 * @returns {sap.ui.mvc.View} the view instance
		 */
		setModel: function (oModel, sName) {
			return this.getView().setModel(oModel, sName);
		},

		/**
		 * Getter for the resource bundle.
		 * @public
		 * @returns {sap.ui.model.resource.ResourceModel} the resourceModel of the component
		 */
		getResourceBundle: function () {
			return this.getOwnerComponent().getModel("i18n").getResourceBundle();
		},

	});
});